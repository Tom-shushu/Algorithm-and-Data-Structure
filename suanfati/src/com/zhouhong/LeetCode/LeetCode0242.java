    public boolean isAnagram(String s, String t) {
        if (s.length() != t.length()){
            return false;
        }
        char[] s1 = s.toCharArray();
        char[] t1 = t.toCharArray();
        Arrays.sort(s1);
        Arrays.sort(t1);
        int s2 = 0;
        while (s2 < s1.length){
            if (s1[s2] == t1[s2]){
                s2++;
            }else {
                return false;
            }
        }
        return true;
    }

